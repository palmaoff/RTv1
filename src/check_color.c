#include "rtv1.h"

t_color	check_color(t_color c)
{
	c.r = c.r > 255 ? 255 : c.r;
	c.g = c.g > 255 ? 255 : c.g;
	c.b = c.b > 255 ? 255 : c.b;
	return (c);
}
