#include "libft.h"

void	ft_putnbr(int n)
{
	if (n > 0)
	{
		if (n / 10 != 0)
			ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
	else if (n == 0)
	{
		ft_putchar('0');
		return ;
	}
	if (n < 0 && n != -2147483648)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	if (n == -2147483648)
		ft_putstr("-2147483648");
}
