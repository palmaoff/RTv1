#include "libft.h"

char	*ft_strnew(size_t size)
{
	int		i;
	char	*s;

	i = 0;
	s = (char *)malloc(size + 1);
	if (!s)
		return (0);
	ft_memset(s, 0, size + 1);
	return (s);
}
